

int sum(int valueIns, int valueConst){
    return valueConst + valueIns;
}

int sub(int valueIns, int valueConst){
    return valueConst - valueIns;
}

int mult(int valueIns, int valueConst){
    return valueConst * valueIns;
}

int res(int valueIns, int valueConst){
    return valueConst % valueIns;

}
